# Naming Convention(작명법)

# 1. 변수 사이에 공백이 허용되지 않음

# 2. 단어 사이는 _(UnderBar)를 사용하여 연결
# Example 
# my greeting = ~ (x) => 1번 조건 불성립
# 공백은 언더바를 사용하여 연결
my_greeting = "Hello, world" 

# 3. 변수를 선언하기 위한 변수명은 숫자 또는 특수문자로 시작이 불가능
# 1_greeting or !_greeting (x) => greeting_1 (o)

# 4. 예약어는 변수로 선언이 불가능
# print를 변수명으로 선언하고 값을 넣어줘도 print는 예약어이기 떄문에 선언이 불가능함

# 5. 가급적 소문자를 사용하는게 좋음
# 대문자를 쓴다고 에러가 발생하는 것이 아니라
# 대문자 변수같은 경우에는 특정한 값을 가지는 데이터를 의미할 수 있기 때문.

