# 숫자 (Numbers)

a = 2
b = 3

print(a+b) # 덧셈

print(a-b) # 뺄셈

print(a*b) # 곱셈

print(a/b) # 나눗셈 -> 실제 나눗셈

print(a ** b) # 제곱수(a의 b제곱)

print((a+b) * b) # 괄호에 있는 연산이 우선

print(a // b) # 몫을 얻는 연산

print(a % b) # 나머지연산


# Float(실수) - 십진 부동 소수점

